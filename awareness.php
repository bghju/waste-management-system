    <h3>Types of Waste</h3>

    <h4>♻ Dry Waste</h4>
    <p>
        Includes paper, plastic, glass, metal, and cardboard.
        These wastes can be recycled and reused.
    </p>

    <h4>🌱 Wet Waste</h4>
    <p>
        Includes food waste, vegetable peels, and organic waste.
        Wet waste can be converted into compost.
    </p>

    <h4>💻 E-Waste</h4>
    <p>
        Includes old mobile phones, batteries, chargers, and electronic items.
        E-waste should be disposed of safely to avoid pollution.
    </p>

    <h3>Waste Segregation on Campus</h3>
    <ul>
        <li>Separate dry and wet waste</li>
        <li>Use labeled dustbins</li>
        <li>Do not mix e-waste with regular waste</li>
        <li>Follow campus waste rules</li>
    </ul>

    <h3>What Happens After Collection?</h3>
    <p>
        Waste collected inside the campus is stored safely.
